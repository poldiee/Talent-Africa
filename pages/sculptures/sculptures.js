$(document).ready(function () {
    $("#more-sudi").click(function () {
        $("#sudi-slider").show();
        $("#sudi").hide();
        $("#husda").hide();
        $("#tunu").hide();
        $("#babu").hide();
        $("#ngurumo").hide();
    })

    $("#more-husda").click(function () {
        $("#husda-slider").show();
        $("#sudi").hide();
        $("#husda").hide();
        $("#tunu").hide();
        $("#babu").hide();
        $("#ngurumo").hide();
    })

    $("#more-tunu").click(function () {
        $("#tunu-slider").show();
        $("#sudi").hide();
        $("#husda").hide();
        $("#tunu").hide();
        $("#babu").hide();
        $("#ngurumo").hide();
    })
    $("#more-babu").click(function () {
        $("#babu-slider").show();
        $("#sudi").hide();
        $("#husda").hide();
        $("#tunu").hide();
        $("#babu").hide();
        $("#ngurumo").hide();
    })
    $("#more-ngurumo").click(function () {
        $("#ngurumo-slider").show();
        $("#sudi").hide();
        $("#husda").hide();
        $("#tunu").hide();
        $("#babu").hide();
        $("#ngurumo").hide();
    })
});



